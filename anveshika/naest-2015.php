<h2>About the Test and Registration Process</h2>
<p>
NAEST is a two level test (1) School Level, Class 9 to Class 12, and (2) College Level, BSc/MSc. The NAEST involves (1) screening test on 8th Aug, (2) Prelim on Aug 16th and 17th (3) Semi Finals on 30th Sep and (4) Final on 1st Oct. Click <a href="anveshika-national-test.html">NAEST Genral Information</a> to know more.
</p>
<p>
This website is primarily meant for the students from the State of Telangana. Click <a href="anveshika-national-network.html">Anveshika National Network</a> to find coordinators details from other states/places. You may fill the <a href="contact-us.html">Contact Us</a> form to let us connected with coordinators from other states.
</p>
<p>
In Telangana State, the registration for the test can be done through
<ol>
	<li>Nomination by the School/College Principal (Click <a href="anveshika/naest-2015/invitation-letter-to-the-principal.pdf">Invitation Letter to the Principal</a> by Dr HC Verma and Local Coordinator) </li>
	<li>Self Registration in Special Cases.</li>
</ol>
 The interested principal, physics teacher and student may fill the form given here to initiate the registration process ...
</p>
<p>
In Telengana State, we are planning to establish screening test centres at all district headquarters i.e., (1) Adilabad (2) Hyderabad (3) Karimnagar (4) Khammam (5) Mahboobnagar (6) Medak (7) Nalgonda (8) Nizamabad (9) Rangareddy (10) Warangal. The details will be worked out as time progresses.
