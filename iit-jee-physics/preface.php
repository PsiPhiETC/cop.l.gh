<h1>IIT JEE Physics Book: Preface</h1>
</p>This book provides a comprehensive collection of IIT JEE problems and
their solutions. We have tried to keep our explanations simple so that
any reader, with knowledge of intermediate physics, can understand them
himself, without any external assistance. So, it can be used for self-study.</p>
<p>To us, every problem in this book is a valuable resource to unravel a
deeper understanding of the physical concepts behind it. Time to solve
a problem is immaterial as far as Physics is concerned. Getting the right
answer is often not as important as the process followed to arrive at it.
</p>
<p>The emphasis in this text remains on the correct understanding of the
principles of Physics and on their application to solution of the problems.
If a student seriously attempts all the problems in this book, he/she will
naturally develop the ability to analyze and solve any problem in a simple
and logical manner using few, well-understood principles.
</p>
<p>For the convenience of the students, we have arranged the problems
according to the standard intermediate physics textbook. Some problems
might be based on the concepts explained in multiple chapters. These
questions are placed in a later chapter so that student can solve them by
using the concept(s) from this and the previous chapters. This book can
complement your favorite text book as an advanced problem book.
</p>
<p>The IIT JEE problems fall into one of the nine categories: (i) MCQ
with single answer correct (ii) MCQ with one or more answer(s) correct
(iii) Paragraph based (iv) Assertion Reasoning based (v) Matrix matching
(vi) True False type (vii) Fill in the blanks (viii) Integer Type, and (ix)
Subjective. Each chapter has sections according to these categories. In
each section, the questions are arranged in descending order of year of its
appearance in IIT JEE.
</p>
<p>The solutions are given at the end of each chapter. If you can't solve a
problem, you can always look up the solution. But please, try first to solve
it yourself; then you will learn more and you'll learn faster. Furthermore, if
you think you do know the answer, you should turn to solution and check
it out, just to make sure.
</p>
<p>
This book has a companion website, www.concepts-of-physics.com.
The site will host latest version of the errata list and other useful material.
</p>
