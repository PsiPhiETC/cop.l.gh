<p>
The goal is to provide students with a strong foundation in science. Common, everyday experiences are widely used to illustrate scientific principles. This should make the reading interesting as well as help students realize the intimate relationship between science and nature. When a new equation is derived or a new concept is introduced, generally, a numerical example follows. These in-text numerical examples should help in clarifying the exact meaning of the terms in the equation and in the elaboration of the concept. The main points discussed in a chapter are collected at one place under the heading Points to Remember. This should help students in quickly revising the chapter, but I strongly discourage depending too heavily on this section. In each chapter a separate section on solved problems illustrates how the equations and laws discussed in that chapter can be used in working out problems.
