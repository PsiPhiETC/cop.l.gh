<h1>Dr HC Verma Books </h1>
<h2>Concepts of Physics</h2>
<p>
The book, "Concepts of Physics", is written by Dr H C Verma and published by Bharati Bhavan. The book is divided into two volumes. The first volume covers mechanics, waves, and optics whereas second volume covers thermodynamics, electromagnetism, and modern physics. The book is well received by students due to its wide coverage, simple language, clear concepts, good problems, and no errors. We strongly recommend this book to serious student of physics. The book not only help students in competitive examinations like IITJEE but also make physics enjoyable. 
</p>
<h2>Foundation Science Physics</h2>
<p>
The Foundation Science Physics is written by Dr HC Verma and Published by Bharati Bhawan. There are two books with this title, one for class 9 and other for class 10. The books are written according to the CBSE syllabus for class 9 and class 10.
</p>
