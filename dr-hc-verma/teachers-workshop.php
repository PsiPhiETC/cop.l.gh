<h1>Teachers Workshop by Dr HC Verma</h1>
<p>Dr HC Verma and his group of Senior Resource Persons (SRP) teachers conducts two types of workshops (1) Introductory Interaction Programs (IIP) and (2) National Workshop of Utsahi Physics Teachers (NWUPT). The details are given below:
</p>
<h2>Introductory Interaction Programs (IIP)</h2>
<p>
To give the first introduction of the Physics teaching methods using simple Physics experiments developed by us, we conduct 7-hours workshops with a group of Physics teachers. The optimum number of teachers is 30-60 though we have conducted workshops for more than 100 teachers also.
</p>
<p>
In the first session we emphasize the need to go for experiment-based classroom teaching and prepare the teachers mentally to change their existing chalk and talk techniques.
</p>
<p>
In the next phase we demonstrate a sample of about 20 to 25 Physics experiments at various levels (class 6 to class 12). With each demonstrative experiment, we explain when it can be used and what Physics can be explained using that particular experiment. We normally take 3 hours to show and discuss these experiments.
</p>
<p>
The last session is question and answer session where teachers ask various questions on Physics and we discuss in the group.
</p>
<p>
The whole workshop takes about 6 to 7 hours with a lunch break and one tea break.
</p>
<p>
More details on IIP and the procedure to conduct IIP at your institution is given at <a href="http://www.utsahiphysicsteachers.com/iip/default.htm" target="_blank">Utsahi Physics Teachers</a> website.
</p>
<h2>National Workshop for Utsahi Physics Teachers (NWUPT)</h2>
<p>
The Senior Resource Persons including Prof. H. C. Verma keep doing short term workshops (e.g. IIP) with Physics Teachers round the year. We encourage these teachers to implement new innovative teaching methodologies in their teaching programmes and write to us about their experiences. During these interactions, a number of enthusiastic teachers are identified who have potential and will contribute towards innovations in Physics Teaching. Only such teachers are invited in the 6-day NWUPT held every year in Summers, generally at IIT Kanpur. See <a href="http://www.utsahiphysicsteachers.com/nationalworkshops/default.htm" target="_blank">NWUPT</a> page for more details. Click <a href="http://www.utsahiphysicsteachers.com/reports/annualReports.htm" target="_blank">NWUPT Reports</a> to see reports of previous workshops. 
</p>    
