<h2>Birth and Initial Education</h2>
<p>Dr. H. C. Verma (<span itemprop="additionalName">Harish Chandra Verma</span>) was born in <span itemprop="birthPlace">Darbhanga, Bihar</span>
on the auspicious day of <time itemprop="birthDate" datetime="1952-04-03">Ramnawami in 1952</time> and spent most of his childhood in Patna.
He was trained in basic arithmetic by his father <span itemprop="parent">Shri Ganesh Prasad Verma</span>, who was
a teacher. He had gone through sweet and sour experiences of school education system in India. 
The education up-to high school (10th) could not create sufficient interest in him, making him poor
at studies and even struggle for passing exams. After high school, he joined Patna Science College, a place that moulded a normal school boy in to a confident and bright adolescent. 
</p>
<h2>Formative Years</h2>
<p>
At <span itemprop="alumniOf">Patna Science College</span>, he find a totally different environment.
The teachers were bright, enthusiastic and encouraging. The boy who struggled for passing marks was
transformed into 3rd ranker in Patna University with a B.Sc. (Honours) degree in Physics.
Then he joined <span itemprop="alumniOf">IIT Kanpur</span> for M.Sc. (1975-77) and topped the class
with a performance index of 9.9 out of 10.0. He chose to remain at IIT Kanpur for his doctorate.
The PhD degree was awarded to him in a short span of three years. The faculty members at IIT Kanpur encouraged him to go to US or Europe for greener pasture. But he wanted to go back to Patna - the place where he struggled.
</p>
<h2>At Patna Science College</h2>
<p>
He joined reputed Patna Sciece College as lecturer in 1980. He was asked to teach students at various levels including those in 11th and 12th standard. He observed that even bright students in class are unable to appreciate and enjoy concepts of physics. He contemplated and realized that problem lies in connecting students with the text book he was following, a great book by Resnick and Halliday. The reason was language and cultural differences. The young students from rural background were entangled into language of the book and loose interest before they could reach the concept and its beauty. He could not find an authentic book that makes concepts of physics enjoyable to young Indian students. This motivated him to write the book "Concepts of Physics". This book was a huge success and made H. C. Verma a hero of young Indian students. The reasons for book's success are authenticity (i.e. almost zero error), simple language, interesting numerical examples, and connection with Indian culture. In this book, you can find problems related to rural games like Goli (marble), ear rings of Indian women, etc. The book succeeded in exposing beauty of physics to Indian students. Dr. Verma spent eight years of his life in writing Concepts of Physics. This was the great gift he gave to Indian students, science, and society. He remained in Patna Science college for almost 15 years before coming to IIT Kanpur.
</p>
 
<h2>At IIT Kanpur</h2>
<p>
Dr. Verma joined <span itemprop="affiliation">IIT Kanpur</span> in 1994 as assistant <span itemprop="jobTitle">professor</span>. Here, he taught many courses, guided M. Sc. and PhD students, author a book "Quantum Physics", and pursued research in experimental nuclear physics. While at IIT Kanpur, apart from his regular assignments, he took many socio-educational initiative for the benefit of education and society. Some of these initiative are School Physics Project, Siksha Sopan, and Utsahi Physics Teachers.
</p>

